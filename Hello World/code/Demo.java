public class Demo {
    public static void main(String[] args) {
        // 自动类型转换
        double d = 10;
        System.out.println(d);

        // 定义byte类型
        byte b = 10;
        short s = b;
        int i = b;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);

    }
}